# 1. Functions in Python:
# A function is a block of code which only runs when it is called.
# You can pass data, known as parameters, into a function.
# A function can return data as a result.

# 2. Importance of function
# You use functions in programming to bundle a set of instructions that you want to use repeatedly or that, because of their complexity, are better self-contained in a sub-program and called when needed. That means that a function is a piece of code written to carry out a specified task.

# 3. Calling a function is different. If you define the function timesTwo tso that it multiplies a number by two.. you can then enter “timesTwo(2)” to call the function, or call for it's assistance in multiplying 2 by 2. Calling a function is just using it.

# 4. A parameter is the variable listed inside the parentheses in the function definition. 
# An argument is the value that are sent to the function when it is called.

# 5. A return statement is used to end the execution of the function call and “returns” the result (value of the expression following the return keyword) to the caller. The statements after the return statements are not executed. If the return statement is without any expression, then the special value None is returned

# 6. In any programming language, functions facilitate code reusability. In simple terms, when you want to do something repeatedly, you can define that something as a function and call that function whenever you need to.

# 7. To define a function with multiple parameters, parameter names are placed one after another, separated by commas, within the parentheses of the function definition.


